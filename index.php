              <h4>Fumigación</h4>
              <p>Es importante tener en claro que la <strong>fumigación</strong> solo es un método por el cual se puede realizar un <b>control de plagas</b>, por medio de la aplicación de humo, gases, vapores, o polvos en suspensión.</p>
              <h4>Manejo Integrado de Plagas (MIP)</h4>
              <p>El MIP es la utilización de todos los recursos necesarios, por medio de procedimientos operativos estandarizados, para minimizar los peligros ocasionados por la presencia de <b>plagas</b>.</p>
              <p>A diferencia del <strong>control de plagas</strong> tradicional (sistema reactivo), el <b>MIP</b> es un sistema proactivo que se adelanta a la incidencia del impacto de las plagas.</p>
